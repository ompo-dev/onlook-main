var NUMBER_RE = /^-?(\d+\.?\d*|\.\d+)(px|rem|em|%|vh|vw|vmin|vmax|ch|ex|dvh|dvw|svh|svw|cqi|cqb|cm|mm|in|pt|pc|deg|rad|turn|s|ms|fr)?$/i;
function classifyTokenValue(value) {
  const v = value.trim().toLowerCase();
  if (!v) return "unknown";
  for (const prefix of COLOR_FUNCTIONS) {
    if (v.startsWith(prefix)) return "color";
  }
  if (v.includes("-gradient(")) return "color";
  if (NAMED_COLORS.has(v)) return "color";
  if (NUMBER_RE.test(v)) return "number";
  return "unknown";
}

