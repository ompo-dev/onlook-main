import { useRef as useRef12, useInsertionEffect as useInsertionEffect2, useCallback as useCallback10 } from "react";
function useMotionRef(visualState, visualElement, externalRef) {
  const externalRefContainer = useRef12(externalRef);
  useInsertionEffect2(() => {
    externalRefContainer.current = externalRef;
  });
  const refCleanup = useRef12(null);
  return useCallback10((instance) => {
    if (instance) {
      visualState.onMount?.(instance);
    }
    const ref = externalRefContainer.current;
    if (typeof ref === "function") {
      if (instance) {
        const cleanup = ref(instance);
        if (typeof cleanup === "function") {
          refCleanup.current = cleanup;
        }
      } else if (refCleanup.current) {
        refCleanup.current();
        refCleanup.current = null;
      } else {
        ref(instance);
      }
    } else if (ref) {
      ref.current = instance;
    }
    if (visualElement) {
      instance ? visualElement.mount(instance) : visualElement.unmount();
    }
  }, [visualElement]);
}

