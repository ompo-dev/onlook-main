var velocitySampleDuration = 5;
function getGeneratorVelocity(resolveValue, t, current3) {
  const prevT = Math.max(t - velocitySampleDuration, 0);
  return velocityPerSecond(current3 - resolveValue(prevT), t - prevT);
}

